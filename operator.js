//==> assignment operator
// = += -= *= /= %= 

            var c=60;
            var d=40;
            console.log(c += d);
            console.log(c -= d);
            console.log(c *= d);
            console.log(c /= d);
            console.log(c %= d);

// ==>> logical operator
// &&  || ! 

// && is logical and operator it checks both the conditions are true or not
// || is logical or operator it checks either of the conditions are true or not
// ! is logical not operator it checks the condition is true or false

            var e=0;
            var f=1;
            console.log(e  && f); // false
            console.log(e  || f); // true

// => condition operator
// < > <= >= == != === !==

            var g=10;
            var h=30;
            console.log(g < h); // true
            console.log(g > h); // false
            console.log(g <= h); // true
            console.log(g >= h); // false
            console.log(g == h); // false
            console.log(g === h); // false
            console.log(g != h); // true
            console.log(g !== h); // true


